    private void bNewClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bNewClienteActionPerformed
        // TODO add your handling code here:
        String cog = tCognome.getText();
        String nom = tNome.getText();
        String luo = tLuogonascita.getText();
        //recuperate info da interfaccia
        LocalDate data = LocalDate.now();
        int cod = 1;
        boolean ok = false;
        while (ok==false) {
            cod = generateCode(MAXCODE);
            ok = checkCodNotInElenco(cod);
        }
        //create info supplementari
        Cliente cli = new Cliente(cog, nom, luo, data, cod);
        //creata istanza di oggetto commesso
        elencoClienti.add(cli);
        myCliente = cli;
        lCliente.setText(myCliente.getInfo());

        //assegnato il nuovo commesso al commesso ufficiale del negozio

    }//GEN-LAST:event_bNewClienteActionPerformed

