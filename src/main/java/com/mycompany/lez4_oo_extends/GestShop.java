    public int generateCode(int maxvalue) {
        double ris = 0;
        double rand = Math.random();
        ris = rand * maxvalue;
        return (int) ris;
    }

    private boolean checkCodNotInElenco(int cod) {
        
        for (Cliente e:elencoClienti ){
            if (e.getCodcliente()==cod) {
                System.out.println("codice gia' usato:" +cod);
                return false;
            }
            
        }
        return true;
        
    }

